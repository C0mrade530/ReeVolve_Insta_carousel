def build_topic_strategist_system(brand_profile: dict) -> str:
    """Build topic strategist system prompt from brand profile."""
    positioning = brand_profile.get("positioning", "Эксперт в своей нише")
    niche = brand_profile.get("niche", "экспертный контент")

    # Format target audience
    audience_lines = []
    for persona in brand_profile.get("target_audience", []):
        if isinstance(persona, dict):
            desc = persona.get("persona", "")
            pains = ", ".join(persona.get("pain_points", [])[:3])
            audience_lines.append(f"- {desc}")
            if pains:
                audience_lines.append(f"  Боли: {pains}")
    audience_text = "\n".join(audience_lines) if audience_lines else "Широкая аудитория"

    # Format content categories
    category_lines = []
    for i, cat in enumerate(brand_profile.get("content_topics", []), 1):
        if isinstance(cat, dict):
            name = cat.get("category", f"Категория {i}")
            topics_list = cat.get("topics", [])
            topics_preview = ", ".join(topics_list[:5])
            priority = cat.get("priority", "medium")
            letter = chr(64 + i)
            category_lines.append(
                f"КАТЕГОРИЯ {letter} — {name.upper()} (приоритет: {priority}):\n"
                f"  Примеры тем: {topics_preview}"
            )
    categories_text = "\n\n".join(category_lines) if category_lines else "Генерируй темы на широкую аудиторию."

    # Format tone
    tone = brand_profile.get("tone_of_voice", {})
    style = tone.get("style", "экспертный")
    addressing = tone.get("addressing", "ты")
    examples = tone.get("examples", [])
    examples_text = "\n".join(f'  - "{ex}"' for ex in examples[:5]) if examples else "  (нет)"
    banned = tone.get("banned_phrases", [])
    banned_text = ", ".join(f'"{b}"' for b in banned[:15]) if banned else "(нет)"
    unique = brand_profile.get("unique_phrases", [])
    unique_text = ", ".join(f'"{p}"' for p in unique[:10]) if unique else "(нет)"

